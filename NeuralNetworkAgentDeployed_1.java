    public CardData hookImpl(List<CardData> options, int phaseNumber, boolean isRequired) {
        if (buyInputWeights == null) {
            initializeWeightsFromText(copiedBuyNetwork, 1);
            initializeWeightsFromText(copiedDiscardNetwork, 2);
            initializeWeightsFromText(copiedGainNetwork, 3);
            initializeWeightsFromText(copiedTrashNetwork, 4);
            initializeWeightsFromText(copiedPlayNetwork, 5);
        }

        double hiddenBias = 0.0;
        double outputBias = 0.0;
        double[] inputWeights = null;
        double[] hiddenWeights = null;
        switch (phaseNumber) {
            case 1:
                hiddenBias = buyHiddenBias;
                outputBias = buyOutputBias;
                inputWeights = buyInputWeights;
                hiddenWeights = buyHiddenWeights;
                break;
            case 2:
                hiddenBias = discardHiddenBias;
                outputBias = discardOutputBias;
                inputWeights = discardInputWeights;
                hiddenWeights = discardHiddenWeights;
                break;
            case 3:
                hiddenBias = gainHiddenBias;
                outputBias = gainOutputBias;
                inputWeights = gainInputWeights;
                hiddenWeights = gainHiddenWeights;
                break;
            case 4:
                hiddenBias = trashHiddenBias;
                outputBias = trashOutputBias;
                inputWeights = trashInputWeights;
                hiddenWeights = trashHiddenWeights;
                break;
            case 5:
                hiddenBias = playHiddenBias;
                outputBias = playOutputBias;
                inputWeights = playInputWeights;
                hiddenWeights = playHiddenWeights;
                break;
        }

        CardData bestCard = null;
        double bestScore = Double.NEGATIVE_INFINITY;

        for (CardData card : options) {
            double[] inputs = getScaledInputs(card, phaseNumber, isRequired);
            double score = evaluateCard(inputs, hiddenBias, outputBias, inputWeights, hiddenWeights);
            if (score > bestScore) {
                bestScore = score;
                bestCard = card;
            }
        }

        return bestCard;
    }

