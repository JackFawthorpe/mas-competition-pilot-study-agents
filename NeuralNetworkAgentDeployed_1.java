    private double evaluateCard(double[] inputs, double hiddenBias, double outputBias, double[] inputWeights, double[] hiddenWeights) {
        double[] hiddenLayer = new double[HIDDEN_SIZE];
        for (int i = 0; i < HIDDEN_SIZE; i++) {
            hiddenLayer[i] = hiddenBias;
            for (int j = 0; j < INPUT_SIZE; j++) {
                hiddenLayer[i] += inputs[j] * inputWeights[i * INPUT_SIZE + j];
            }
            hiddenLayer[i] = Math.max(0, hiddenLayer[i]); // ReLU
        }

        double output = outputBias;
        for (int i = 0; i < HIDDEN_SIZE; i++) {
            output += hiddenLayer[i] * hiddenWeights[i];
        }

        return output;
    }

    private void initializeWeightsFromText(String weightsText, int phaseNumber) {
        double[] inputWeights;
        double[] hiddenWeights;
        double phaseHiddenBias;
        double phaseOutputBias;

        String[] lines = weightsText.trim().split("\n");

        if (lines.length != ((INPUT_SIZE * HIDDEN_SIZE) + (HIDDEN_SIZE * OUTPUT_SIZE) + (2))) {
            throw new IllegalArgumentException("Invalid number of weights and biases provided.");
        }

        inputWeights = new double[INPUT_SIZE * HIDDEN_SIZE];
        hiddenWeights = new double[HIDDEN_SIZE * OUTPUT_SIZE];
        for (int i = 0; i < INPUT_SIZE * HIDDEN_SIZE; i++) {
            inputWeights[i] = Double.parseDouble(lines[i]);
        }
        for (int i = 0; i < HIDDEN_SIZE * OUTPUT_SIZE; i++) {
            hiddenWeights[i] = Double.parseDouble(lines[INPUT_SIZE * HIDDEN_SIZE + i]);
        }
        phaseHiddenBias = Double.parseDouble(lines[INPUT_SIZE * HIDDEN_SIZE + HIDDEN_SIZE * OUTPUT_SIZE]);
        phaseOutputBias = Double.parseDouble(lines[INPUT_SIZE * HIDDEN_SIZE + HIDDEN_SIZE * OUTPUT_SIZE + 1]);

        switch (phaseNumber) {
            case 1:
                buyHiddenBias = phaseHiddenBias;
                buyOutputBias = phaseOutputBias;
                buyInputWeights = inputWeights;
                buyHiddenWeights = hiddenWeights;
                break;
            case 2:
                discardHiddenBias = phaseHiddenBias;
                discardOutputBias = phaseOutputBias;
                discardInputWeights = inputWeights;
                discardHiddenWeights = hiddenWeights;
                break;
            case 3:
                gainHiddenBias = phaseHiddenBias;
                gainOutputBias = phaseOutputBias;
                gainInputWeights = inputWeights;
                gainHiddenWeights = hiddenWeights;
                break;
            case 4:
                trashHiddenBias = phaseHiddenBias;
                trashOutputBias = phaseOutputBias;
                trashInputWeights = inputWeights;
                trashHiddenWeights = hiddenWeights;
                break;
            case 5:
                playHiddenBias = phaseHiddenBias;
                playOutputBias = phaseOutputBias;
                playInputWeights = inputWeights;
                playHiddenWeights = hiddenWeights;
                break;
        }
    }


